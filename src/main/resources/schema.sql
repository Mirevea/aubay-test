CREATE TABLE SUPERHEROS (
    ID BIGINT not null AUTO_INCREMENT,
    REAL_NAME varchar(255) not null,
    POWER varchar(255) not null,
    POWER_LEVEL int not null,
    REAL_NAME varchar(255) not null,
    HERO_NICKNAME varchar(255) not null,
    ORIGIN varchar(255) not null,
    PRIMARY KEY ( ID )
);
